    NSString *path = [[NSBundle mainBundle] pathForResource:@"extensions" ofType:@"plist"];
    NSDictionary *dicTemp = [[NSDictionary alloc] initWithContentsOfFile:path];
    NSArray *extensions = [dicTemp objectForKey:@"Root"];
